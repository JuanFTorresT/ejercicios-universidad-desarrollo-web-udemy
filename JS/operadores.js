let a = 3, b = 2;
let suma = a + b;
console.log("resultado de la suma = " + suma);
let resta = a - b;
console.log("resultado de la resta = " + resta);
let multiplicacion = a * b;
console.log("resultado de la multiplicacion = " + multiplicacion);
let division = a / b;
console.log("resultado de la division = " + division);
let exponente = a ** b;
console.log("resultado de la suma = " + exponente);
let residuo = a % b;
console.log("resultado de la suma = " + residuo);
let incremento = ++a;
console.log("resultado de la suma = " + incremento);
//aqu√≠ a vale 4
let decremento = --a;
console.log("resultado de la suma = " + decremento);

